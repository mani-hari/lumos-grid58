export default function ProjectView() {
  const { id } = useParams()
  const navigate = useNavigate()
  const [project, setProject] = useState(null)
  const [skills, setSkills] = useState([])
  const [selectedSkill, setSelectedSkill] = useState(null)
  const [content, setContent] = useState('')
  const [dirty, setDirty] = useState(false)
  const [saving, setSaving] = useState(false)
  const [loading, setLoading] = useState(true)
  const [viewMode, setViewMode] = useState('edit') // edit | preview

  // Analysis state
  const [analysisStatus, setAnalysisStatus] = useState('idle')
  const [analysisResult, setAnalysisResult] = useState(null)
  const [analysisExpanded, setAnalysisExpanded] = useState(false)
  const analysisRanRef = useRef(false)

  useEffect(() => {
    loadProject()
  }, [id])

  async function loadProject() {
    try {
      const res = await api.getProject(id)
      const data = res.data
      setProject(data)
      setSkills(data.skills || [])

      // Select first skill by default
      if (data.skills?.length > 0) {
        selectSkillDirect(data.skills[0])
      }
    } catch (err) {
      console.error('Failed to load project:', err)
      navigate('/')
    } finally {
      setLoading(false)
    }
  }

  // Auto-start analysis once project loads
  useEffect(() => {
    if (skills.length === 0 || analysisRanRef.current) return
    analysisRanRef.current = true
    runAnalysis(skills)
  }, [skills])

  async function runAnalysis(skillList) {
    setAnalysisStatus('running')
    try {
      const payload = {
        skills: skillList.map((s) => ({ id: s.id, name: s.name, content: s.content })),
      }
      const res = await api.analyzeProject(payload)
      const data = res.data
      const analysis = data.analysis || data

      setAnalysisResult(analysis)
      setAnalysisStatus('done')

      // Create doso_readme document from analysis
      createDosoReadme(analysis, skillList)
    } catch {
      setAnalysisStatus('error')
    }
  }

  async function createDosoReadme(analysis, skillList) {
    try {
      const markdownContent = formatAnalysisMarkdown(analysis, project?.name || 'Project', skillList)
      const res = await api.createSkill({
        name: 'doso_readme',
        description: 'Auto-generated project analysis by doso.dev',
        content: markdownContent,
        category: 'General',
        tags: ['doso_readme', 'analysis'],
        is_public: false,
        project_id: id,
      })

      // Add to the project's skill list
      const newSkill = res.data
      if (newSkill?.id) {
        // Link it to the project
        try {
          await api.addSkillToProject(id, newSkill.id)
        } catch {
          // ignore if linking fails
        }
        setSkills((prev) => [...prev, newSkill])
      }
    } catch (err) {
      console.error('Failed to create doso_readme:', err)
    }
  }

  function selectSkillDirect(skill) {
    setSelectedSkill(skill)
    setContent(skill.content || '')
    setDirty(false)
    setViewMode('edit')
  }

  function selectSkill(skill) {
    if (dirty) {
      if (!confirm('You have unsaved changes. Discard?')) return
    }
    selectSkillDirect(skill)
  }

  function handleContentChange(val) {
    setContent(val)
    setDirty(true)
  }

  const handleSave = useCallback(async () => {
    if (!selectedSkill || !dirty) return
    setSaving(true)
    try {
      const res = await api.updateSkill(selectedSkill.id, { ...selectedSkill, content })
      const updated = res.data
      setSelectedSkill(updated)
      setSkills((prev) => prev.map((s) => (s.id === updated.id ? updated : s)))
      setDirty(false)
    } catch (err) {
      alert('Failed to save: ' + err.message)
    } finally {
      setSaving(false)
    }
  }, [selectedSkill, content, dirty])

  // Cmd+S
  useEffect(() => {
    function onKeyDown(e) {
      if ((e.metaKey || e.ctrlKey) && e.key === 's') {
        e.preventDefault()
        handleSave()
      }
    }
    window.addEventListener('keydown', onKeyDown)
    return () => window.removeEventListener('keydown', onKeyDown)
  }, [handleSave])

  if (loading) {
    return (
      <div className="h-full flex items-center justify-center">
        <div className="animate-pulse-bar">
          <Loader className="w-6 h-6" style={{ color: '#888' }} />
        </div>
      </div>
    )
  }

  const tree = buildFileTree(skills)
  const breadcrumbs = getBreadcrumbs(selectedSkill, project?.name || 'Project')
  const tokenEstimate = Math.ceil((content?.length || 0) / 4)

  return (
    <div className="h-full flex flex-col" style={{ position: 'relative' }}>
      {/* Top bar: breadcrumbs + analysis pill + save */}
      <div
        className="flex items-center justify-between shrink-0"
        style={{ borderBottom: '1px solid #e5e5e5', padding: '0 16px', height: 44, background: '#fff', position: 'relative', zIndex: 21 }}
      >
        <div className="flex items-center gap-1 min-w-0">
          <button
            onClick={() => navigate('/')}
            style={{ padding: 4, background: 'none', border: 0, cursor: 'pointer', color: '#888', display: 'flex' }}
            title="Home"
          >
            <Home className="w-3.5 h-3.5" />
          </button>
          {breadcrumbs.map((crumb, i) => (
            <span key={i} className="flex items-center gap-1" style={{ fontSize: 12, color: i === breadcrumbs.length - 1 ? '#000' : '#888' }}>
              <ChevronRight className="w-3 h-3" style={{ color: '#ddd' }} />
              <span style={{ fontWeight: i === breadcrumbs.length - 1 ? 600 : 400 }} className="truncate">{crumb}</span>
            </span>
          ))}
          {dirty && (
            <span style={{ fontSize: 10, color: '#D4A843', fontWeight: 500, marginLeft: 8 }}>Unsaved</span>
          )}
        </div>

        <div className="flex items-center gap-3">
          {/* Analysis pill */}
          <AnalysisPill
            status={analysisStatus}
            result={analysisResult}
            expanded={analysisExpanded}
            onToggle={() => setAnalysisExpanded(!analysisExpanded)}
          />

          {selectedSkill && (
            <>
              <span style={{ fontSize: 10, color: '#aaa', fontFamily: 'JetBrains Mono, monospace' }}>
                ~{tokenEstimate.toLocaleString()} tokens
              </span>
              <div className="flex" style={{ border: '1px solid #e5e5e5', borderRadius: 4, overflow: 'hidden' }}>
                <button
                  onClick={() => setViewMode('edit')}
                  style={{
                    padding: '4px 10px', fontSize: 11, fontWeight: 500, border: 0, cursor: 'pointer',
                    background: viewMode === 'edit' ? '#000' : '#fff',
                    color: viewMode === 'edit' ? '#fff' : '#888',
                  }}
                >
                  Edit
                </button>
                <button
                  onClick={() => setViewMode('preview')}
                  style={{
                    padding: '4px 10px', fontSize: 11, fontWeight: 500, border: 0, cursor: 'pointer',
                    background: viewMode === 'preview' ? '#000' : '#fff',
                    color: viewMode === 'preview' ? '#fff' : '#888',
                    borderLeft: '1px solid #e5e5e5',
                  }}
                >
                  Preview
                </button>
              </div>
              <button
                onClick={handleSave}
                disabled={saving || !dirty}
                className="btn-primary btn-sm"
                style={{ opacity: dirty ? 1 : 0.4 }}
              >
                <Save className="w-3.5 h-3.5" />
                {saving ? 'Saving...' : 'Save'}
              </button>
            </>
          )}
        </div>
      </div>

      {/* Analysis dropdown (overlays below breadcrumb bar) */}
      {analysisExpanded && analysisResult && (
        <AnalysisDropdown
          result={analysisResult}
          onClose={() => setAnalysisExpanded(false)}
        />
      )}

      {/* Main area: tree + editor */}
      <div className="flex-1 flex overflow-hidden" style={{ position: 'relative' }}>
        {/* Left: File Tree */}
        <div
          className="shrink-0 overflow-y-auto"
          style={{
            width: 220,
            borderRight: '1px solid #e5e5e5',
            background: '#fafafa',
            padding: '8px 0',
          }}
        >
          {/* Project header */}
          <div style={{ padding: '6px 12px 10px', borderBottom: '1px solid #e5e5e5', marginBottom: 4 }}>
            <div className="flex items-center gap-2">
              <FolderOpen className="w-3.5 h-3.5" style={{ color: '#888' }} />
              <span style={{ fontSize: 12, fontWeight: 600, color: '#000' }} className="truncate">
                {project?.name}
              </span>
            </div>
            {project?.description && (
              <p style={{ fontSize: 10, color: '#888', marginTop: 2, paddingLeft: 18 }} className="truncate">
                {project.description}
              </p>
            )}
          </div>

          {/* Tree */}
          {skills.length === 0 ? (
            <p style={{ padding: '12px 16px', fontSize: 12, color: '#aaa' }}>No files yet</p>
          ) : (
            Object.entries(tree).map(([name, node]) => (
              <TreeNode
                key={name}
                name={name}
                node={node}
                selectedId={selectedSkill?.id}
                onSelect={selectSkill}
              />
            ))
          )}
        </div>

        {/* Center: Editor / Preview */}
        <div className="flex-1 flex flex-col min-w-0 overflow-hidden" style={{ position: 'relative' }}>
          {!selectedSkill ? (
            <div className="flex-1 flex items-center justify-center">
              <div style={{ textAlign: 'center', color: '#aaa' }}>
                <FileText className="w-10 h-10 mx-auto" style={{ opacity: 0.3, marginBottom: 8 }} />
                <p style={{ fontSize: 14 }}>Select a file to edit</p>
              </div>
            </div>
          ) : viewMode === 'preview' ? (
            <div className="flex-1 overflow-y-auto" style={{ padding: 24 }}>
              <MarkdownPreview content={content} />
            </div>
          ) : (
            <textarea
              value={content}
              onChange={(e) => handleContentChange(e.target.value)}
              className="skill-editor w-full h-full flex-1"
              spellCheck={false}
              style={{ resize: 'none' }}
            />
          )}

          {/* Floating toolbar (right side) */}
          <FloatingToolbar skill={selectedSkill} />
        </div>
      </div>
    </div>
  )
}
