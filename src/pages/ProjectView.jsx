// ── Helpers ──────────────────────────────────────────────

function buildFileTree(skills) {
  const root = {}
  for (const skill of skills) {
    const path = skill.tags?.[0] || skill.name
    const parts = path.split('/')
    let node = root
    for (let i = 0; i < parts.length - 1; i++) {
      if (!node[parts[i]]) node[parts[i]] = { __children: {} }
      if (!node[parts[i]].__children) node[parts[i]].__children = {}
      node = node[parts[i]].__children
    }
    const filename = parts[parts.length - 1]
    node[filename] = { __skill: skill }
  }
  return root
}

function getBreadcrumbs(skill, projectName) {
  if (!skill) return [projectName]
  const path = skill.tags?.[0] || skill.name
  return [projectName, ...path.split('/')]
}

function CopyButton({ text }) {
  const [copied, setCopied] = useState(false)
  const handleCopy = async () => {
    await navigator.clipboard.writeText(text)
    setCopied(true)
    setTimeout(() => setCopied(false), 2000)
  }
  return (
    <button
      onClick={handleCopy}
      style={{ padding: 4, background: 'transparent', border: 0, cursor: 'pointer', color: copied ? '#000' : '#aaa', display: 'flex' }}
      title="Copy"
    >
      {copied ? <Check className="w-3 h-3" /> : <Copy className="w-3 h-3" />}
    </button>
  )
}

// Format analysis result as a markdown document
function formatAnalysisMarkdown(analysis, projectName, skills) {
  const lines = []
  lines.push(`# doso analysis: ${projectName}`)
  lines.push('')
  lines.push(`*Auto-generated by doso.dev on ${new Date().toLocaleDateString()}*`)
  lines.push('')

  if (analysis.project_summary) {
    lines.push('## Project Overview')
    lines.push('')
    lines.push(analysis.project_summary)
    lines.push('')
  }

  if (analysis.skill_summaries?.length > 0) {
    lines.push('## Skills')
    lines.push('')
    for (const s of analysis.skill_summaries) {
      lines.push(`### ${s.name}`)
      lines.push(s.purpose || '')
      if (s.strengths?.length > 0) {
        lines.push(`- **Strengths:** ${s.strengths.join(', ')}`)
      }
      if (s.weaknesses?.length > 0) {
        lines.push(`- **Weaknesses:** ${s.weaknesses.join(', ')}`)
      }
      lines.push('')
    }
  }

  const score = analysis.health_score
  if (score) {
    lines.push('## Health Score')
    lines.push('')
    lines.push(`**Overall: ${score.overall}/100**`)
    if (score.breakdown) {
      lines.push(`- Clarity: ${score.breakdown.clarity}/100`)
      lines.push(`- Consistency: ${score.breakdown.consistency}/100`)
      lines.push(`- Coverage: ${score.breakdown.coverage}/100`)
      lines.push(`- Efficiency: ${score.breakdown.efficiency}/100`)
    }
    if (score.summary) lines.push(`\n${score.summary}`)
    lines.push('')
  }

  if (analysis.suggestions?.length > 0) {
    lines.push('## Improvement Suggestions')
    lines.push('')
    for (const s of analysis.suggestions) {
      const priority = s.priority === 'high' ? '!!!' : s.priority === 'medium' ? '!!' : '!'
      lines.push(`### ${priority} ${s.title}`)
      if (s.skill_name) lines.push(`*Applies to: ${s.skill_name}*`)
      lines.push('')
      lines.push(s.description)
      lines.push('')
    }
  }

  if (analysis.coverage_gaps?.length > 0) {
    lines.push('## Coverage Gaps')
    lines.push('')
    for (const g of analysis.coverage_gaps) {
      lines.push(`- **${g.gap}**: ${g.suggestion}`)
    }
    lines.push('')
  }

  if (analysis.cross_skill_contradictions?.length > 0) {
    lines.push('## Contradictions')
    lines.push('')
    for (const c of analysis.cross_skill_contradictions) {
      lines.push(`- **${c.skill_a} vs ${c.skill_b}** (${c.severity}): ${c.description}`)
    }
    lines.push('')
  }

  return lines.join('\n')
}

// ── File Tree Component ──────────────────────────────────

function TreeNode({ name, node, depth = 0, selectedId, onSelect }) {
  const [open, setOpen] = useState(true)

  if (node.__skill) {
    const skill = node.__skill
    const isSelected = selectedId === skill.id
    const isDoso = name === 'doso_readme' || name === 'doso_readme.md'
    return (
      <button
        onClick={() => onSelect(skill)}
        className="w-full text-left flex items-center gap-1.5"
        style={{
          padding: '5px 8px',
          paddingLeft: 8 + depth * 16,
          fontSize: 12,
          color: isSelected ? '#000' : isDoso ? '#D4A843' : '#555',
          fontWeight: isSelected ? 600 : 400,
          fontStyle: isDoso ? 'italic' : 'normal',
          background: isSelected ? '#f0f0f0' : 'transparent',
          border: 0,
          borderRadius: 3,
          cursor: 'pointer',
          transition: 'background 80ms',
        }}
        onMouseEnter={(e) => { if (!isSelected) e.currentTarget.style.background = '#f5f5f5' }}
        onMouseLeave={(e) => { if (!isSelected) e.currentTarget.style.background = 'transparent' }}
      >
        {isDoso ? (
          <Sparkles className="w-3.5 h-3.5 shrink-0" style={{ color: '#D4A843' }} />
        ) : (
          <FileText className="w-3.5 h-3.5 shrink-0" style={{ color: '#D4A843' }} />
        )}
        <span className="truncate">{name}</span>
      </button>
    )
  }

  // Directory node
  const children = node.__children || node
  const entries = Object.entries(children).filter(([k]) => k !== '__children')
  if (entries.length === 0) return null

  return (
    <div>
      <button
        onClick={() => setOpen(!open)}
        className="w-full text-left flex items-center gap-1.5"
        style={{
          padding: '5px 8px',
          paddingLeft: 8 + depth * 16,
          fontSize: 12,
          color: '#666',
          fontWeight: 500,
          background: 'transparent',
          border: 0,
          cursor: 'pointer',
        }}
      >
        {open ? <ChevronDown className="w-3 h-3 shrink-0" /> : <ChevronRight className="w-3 h-3 shrink-0" />}
        <FolderOpen className="w-3.5 h-3.5 shrink-0" style={{ color: '#888' }} />
        <span className="truncate">{name}</span>
      </button>
      {open && entries.map(([key, child]) => (
        <TreeNode
          key={key}
          name={key}
          node={child}
          depth={depth + 1}
          selectedId={selectedId}
          onSelect={onSelect}
        />
      ))}
    </div>
  )
}

